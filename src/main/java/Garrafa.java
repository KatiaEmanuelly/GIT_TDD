public class Garrafa {

    private int capacidade;
    private int volume;

    public Garrafa(int capacidade, int volume) {
        this.capacidade = capacidade;
        this.volume = volume;
    }

    int getCapacidade() {
        return capacidade;
    }

    int getVolume() {
        return volume;
    }
}
